		// $text = str_replace('Озерная улица', ' ', $text);
		// $text = str_replace('улица Озерная', ' ', $text);
		// $text = str_replace('ул. Озерная', ' ', $text);
		// $text = str_replace('ТРЦ Большевик', ' ', $text);
		// $text = str_replace('торговый центр Большевик', ' ', $text);
		
		foreach($this->region_auto_select as $region_id => $city_list) {
			if ($this->def_city && $this->regional_center[$region_id] != $this->def_city) {
				continue;
			}
			foreach ($city_list as $city) {
				if ($text == $city || preg_match('#([\s!?,:\)\.-])+(' . $city . ')+([\s!?,:\)\.-])+#si', $text, $matches)) {
				//if (strpos($text, $city) !== false) {
					return array(
						'row_type' => $region_id,
						'city_name' => $city,
					);
				}
				if ($text == $city || preg_match('#([\s!?,:\)\.-])+(' . $city . 'е'. ')+([\s!?,:\)\.-])+#si', $text, $matches)) {
				//if (strpos($text, $city) !== false) {
					return array(
						'row_type' => $region_id,
						'city_name' => $city,
					);
				}
			}
		}
		return false;
	}
